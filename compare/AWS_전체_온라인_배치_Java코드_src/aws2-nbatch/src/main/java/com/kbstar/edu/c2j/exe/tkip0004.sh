CURRDIR=$(realpath $(dirname $0))
cd $CURRDIR
SHELL_NAME=$(basename $0)
WAS_DOMAIN=$(echo $CURRDIR | cut -d/ -f 3)

. /fsapp/$WAS_DOMAIN/sqc/runtime/batch/config/shell/setEnv.sh

#====================================================
# 배치 프로그램 클래스명 설정
#====================================================
NC_BATCH_CLASS1=com.kbstar.edu.c2j.batch.BUBIP0004
