#====================================================
# 배치 프로그램 실행
#====================================================
. $NEXCORE_HOME/batch/config/shell/batchrun.sh ${ARGS}

#====================================================
# 실행결과 에러/정상 여부 판단. (종료코드가 0이면 정상, 그외 에러)
#====================================================

echo [`date +"%Y-%m-%d %H:%M:%S"`] ENDED JAVA BATCH. Exit:${NC_RETURNCODE}
